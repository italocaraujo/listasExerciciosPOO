package Lista03;

public class Exercicio29 {
    public static void main(String[] args) {
        System.out.println("Imprime de 1 a 2000: "); // Exibe uma mensagem informando o propósito do programa
        System.out.println(); // Imprime uma linha em branco para melhorar a formatação da saída

        for (int i = 1; i <= 2000; i++) { // Início do loop que irá imprimir números de 1 a 2000
            System.out.println(i); // Imprime o valor atual da variável i no console
        }
    }
}
